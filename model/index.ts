/**
 * Get the milestone of a child given their age in months
 *
 * @export
 * @param {number} age
 * @return {*}  {(Milestone | undefined)}
 */
export function getMilestonesByAge(age: number): Milestone | undefined {
	return milestones.find((milestone) => milestone.months === age);
}

/**
 * Get the individual sub-milestone categories that make up the bigger milestone
 *
 * @export
 * @param {number} age
 * @param {milestoneCategory} categoryCode
 * @return {*}  {categoryMilestones[]}
 */
export function getMilestonesByAgeAndCategory(
	age: number,
	categoryCode: milestoneCategory
): categoryMilestone[] {
	return (
		getMilestonesByAge(age)?.categories.find(
			(category) => category.code === categoryCode
		)?.milestones || []
	);
}

/**
 * Get a single milestone given its code, its category code, milestone code and the age in months
 *
 * @export
 * @param {number} age
 * @param {milestoneCategory} categoryCode
 * @param {string} milestoneCode
 * @return {*}  {(categoryMilestones | undefined)}
 */
export function getMilestoneByAgeAndCategory(
	age: number,
	categoryCode: milestoneCategory,
	milestoneCode: string
): categoryMilestone | undefined {
	return getMilestonesByAgeAndCategory(age, categoryCode).find(
		(milestones) => milestones.code === milestoneCode
	);
}
